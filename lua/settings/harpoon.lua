function CreateNewFilePopup()
  vim.ui.input({ prompt = 'Name of new file: ' }, function(input)
    if input and input ~= '' then
      local current_dir = vim.fn.expand('%:p:h')
      local full_path = current_dir .. '/' .. input
      vim.cmd('edit ' .. full_path)
	  harpoon:list():add() 
    end
  end)
end

